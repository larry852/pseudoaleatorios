package Logic;

public class NumeroKS {

	private double randomnumber;
	private double accumulatedprovision; //distribucuion acumulada
	private double difference;
	
	public double getRandomnumber() {
		return randomnumber;
	}
	public void setRandomnumber(double randomnumber) {
		this.randomnumber = randomnumber;
	}
	public double getAccumulatedprovision() {
		return accumulatedprovision;
	}
	public void setAccumulatedprovision(double accumulatedprovision) {
		this.accumulatedprovision = accumulatedprovision;
	}
	public double getDifference() {
		return difference;
	}
	public void setDifference(double difference) {
		this.difference = difference;
	}
	
}
